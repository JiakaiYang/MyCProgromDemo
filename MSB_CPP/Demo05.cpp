#include <iostream>

using namespace std;
//位运算符 ~ & | >>  <<
int main()
{   int i = (-1)>>3;
//-1
//1000 0000 0000 0000 0000 0000 0000 0001  原码
//1111 1111 1111 1111 1111 1111 1111 1110  反码     除符号位，其余全部取反
//1111 1111 1111 1111 1111 1111 1111 1111  补码     反码加1
//>>逻辑右移右边溢出，左边补零    算术右移：右边溢出，左边为1补1，为0补0
    int b = 1>>3;
    cout<<i<<endl; //输出结果为-1 说明-1>>3是算术右移  
    cout<<b<<endl;  //结果为0
    return 0;
}